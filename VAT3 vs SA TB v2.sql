SELECT
EXTERNAL_SA_ID
,TRUE_BAL_AMT
INTO #SA
FROM
MISUserData.[UK\PB32946].VCRO_SERV_ACCT_01112020
WHERE
TRUE_BAL_AMT < '0'

SELECT
EXTERNAL_SA_ID
,(NET_AMOUNT+VAT_AMOUNT) AMT
INTO #VAT3
FROM
VMI_VAT3_DETAIL_CURRENT
WHERE
CD_BUSE IN ('ESU','GSS')

SELECT
SUM(TRUE_BAL_AMT)
FROM
#SA

SELECT
SUM(AMT)
FROM
#VAT3

SELECT
TOP 1000 *

FROM
#VAT3
FULL OUTER JOIN
#SA
ON #VAT3.EXTERNAL_SA_ID = #SA.EXTERNAL_SA_ID

WHERE
AMT <> TRUE_BAL_AMT