SELECT 
account_no	ACC_NO
,CASE WHEN SUM(CONVERT(DECIMAL(11,2),ROUND(GAP,2))) > '0' THEN 'DEBIT' ELSE 'CREDIT' END	CR_DR
,CONVERT(DECIMAL(11,2),ROUND(GAP,2)) GAP

INTO #BOOST_GAP

FROM
MISUserData.[UK\PB32946].BOOST_GAP_JUNE20

WHERE
GAP <> ''
AND GAP <> '0'

GROUP BY
account_no
,Gap

SELECT
CR_DR
,SUM(GAP)	GAP

FROM
#BOOST_GAP

GROUP BY
CR_DR